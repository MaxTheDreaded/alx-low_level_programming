#include "main.h"

/**
 * _add - adds two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int _add(int a, int b)
{
return (a + b);
}

/**
 * _sub - subtracts two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int _sub(int a, int b)
{
return (a - b);
}

/**
 * _mul - multiplies two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int _mul(int a, int b)
{
return (a * b);
}

/**
 * _div - divides two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of a and b
 */
int _div(int a, int b)
{
return (a / b);
}

/**
 * _mod - divides two integers and returns the remainder
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of a and b
 */
int _mod(int a, int b)
{
return (a % b);
}
