#include "main.h"

/**
 * add - adds two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * sub - subtracts two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - multiplies two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int mul(int a, int b)
{
return (a * b);
}

/**
 * div - divides two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of a and b
 */
int div(int a, int b)
{
return (a / b);
}

/**
 * mod - divides two integers and returns the remainder
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of a and b
 */
int mod(int a, int b)
{
return (a % b);
}
